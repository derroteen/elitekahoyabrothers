
ALTER TABLE public.profiles ADD COLUMN IF NOT EXISTS sort_order INTEGER;

UPDATE public.profiles SET sort_order = CASE membership_no
  WHEN 'EKB001' THEN 1
  WHEN 'EKB010' THEN 2
  WHEN 'EKB002' THEN 3
  WHEN 'EKB014' THEN 4
  WHEN 'EKB009' THEN 5
  WHEN 'EKB008' THEN 6
  WHEN 'EKB011' THEN 7
  WHEN 'EKB012' THEN 8
  WHEN 'EKB003' THEN 9
  WHEN 'EKB005' THEN 10
  WHEN 'EKB013' THEN 11
  WHEN 'EKB004' THEN 12
  WHEN 'EKB007' THEN 13
  WHEN 'EKB015' THEN 14
  WHEN 'EKB006' THEN 15
  ELSE 9999
END;

CREATE INDEX IF NOT EXISTS profiles_sort_order_idx ON public.profiles(sort_order);
